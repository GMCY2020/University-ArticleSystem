package Beans;

public class Article {
    private String art_id;
    private String art_title;
    private String art_author;
    private String art_detail;
    private String create_time;
    private String update_time;

    public String getArt_id() {
        return art_id;
    }

    public void setArt_id(String art_id) {
        this.art_id = art_id;
    }

    public String getArt_title() {
        return art_title;
    }

    public void setArt_title(String art_title) {
        this.art_title = art_title;
    }

    public String getArt_author() {
        return art_author;
    }

    public void setArt_author(String art_author) {
        this.art_author = art_author;
    }

    public String getArt_detail() {
        return art_detail;
    }

    public void setArt_detail(String art_detail) {
        this.art_detail = art_detail;
    }

    public String getCreate_time() {
        return create_time;
    }

    public void setCreate_time(String create_time) {
        this.create_time = create_time;
    }

    public String getUpdate_time() {
        return update_time;
    }

    public void setUpdate_time(String update_time) {
        this.update_time = update_time;
    }
}
